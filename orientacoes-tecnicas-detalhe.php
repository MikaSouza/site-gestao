DO SISTEMA SIAPESweb – CONCURSOS
Recentemente, o Tribunal de Contas do Estado do Rio Grande do Sul
instituiu e regulamentou a utilização do novo sistema SIAPESweb Concursos,
com o objetivo de aperfeiçoar o controle preventivo e concomitante dos atos
relacionados à contratação de pessoal, proporcionando maior segurança
jurídica e consequentemente mais eficiência aos processos.
A nova ferramenta determina o envio de informações ao TCE sobre
concursos públicos e processos seletivos, bem como os atos de admissão
derivados. Tudo de forma estruturada, concomitantemente à sua realização.
Os procedimentos de apreciação da regularidade de concursos públicos e de
processos seletivos públicos, foram regulamentados por meio da Resolução
TCE/RS nº 1.117/2019, que atualizou a Resolução TCE/RS nº 1.051/2015.
Para os Órgãos Públicos Municipais (Executivo, Legislativo e
Administração Indireta), o preenchimento do sistema tornou-se obrigatório
para todos os concursos e processos seletivos públicos realizados partir de 03
de fevereiro de 2020. Os prazos para envio foram estabelecidos pela Instrução
Normativa TCE/RS nº 01/2020, a qual determina também as informações que
devem ser enviadas e a técnica de envio, em cada uma das fases.
Alerta-se que o não envio das informações e documentos via SIAPESweb
– Concursos na forma e prazos estabelecidos, poderá ensejar na aplicação de
multas previstas na Lei Orgânica da Corte de Contas Estadual, além de,
conforme a hipótese, a juízo do TCE/RS, repercutir negativamente na
apreciação de contas do Gestor.

DAS ATRIBUIÇÕES E RESPONSABILIDADES
A recomendação pertinente ao município é que seja disponibilizado um
servidor responsável, lotado na área de Recursos Humanos, para atualização e
lançamento dos dados no sistema, enquanto a UCCI tem a responsabilidade
pela verificação da exatidão das informações lançadas no SIAPESweb –
Concursos, além de responder aos questionamentos do TCE que por ventura
surgirem.
Outro ponto importante a ser observado é que a UCCI poderá ser
demandada pela auditoria do TCE/RS. A solicitação ocorrerá mediante e-mail
disparado pelo próprio sistema. Ao receber a demanda e acessar o sistema, a
UCCI localizará a lista de verificações. Salientamos que o prazo para resposta é
de 3 (três) dias.
A omissão da UCCI em responder ao requerimento da auditoria, poderá
importar em responsabilização solidária pelas irregularidades verificadas, nos
termos legais e regimentais do TCE/RS.
Alguns tópicos a serem lançados no sistema e que podem ser alvo de
verificação por parte da UCCI, são:
a) Existência de legislação local a respeito de concursos e processos
seletivos públicos;
b) Previsão legal para a realização de concurso ou processo seletivo
público;
c) Previsão legal sobre a reserva de vagas ou isenções;
d) Os cargos e empregos;
e) Legislação de criação ou alteração de cargos;
f) Códigos de identificação de cargo;
g) Quantidade de vagas por cargo;
h) Membros da Comissão Organizadora / Portaria de nomeação;
i) Cargos cujas vagas são ofertadas dentro de uma área geográfica, como
o cargo de Agente Comunitário de Saúde, por exemplo;
j) Banca Examinadora (membros);
k) Atos de Inscrição;
l) Relação de Inscritos;
m)Datas de realização das provas;
n) Atos de homologação do concurso;
